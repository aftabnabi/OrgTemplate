				<p style="margin:10px 5px"><img src="images/ad2f.jpg" style="float:right" />The Government of Punjab in the Labour and Human Resource Department intends to undertake a 5 year project (2011-16) for elimination of worst forms of child labour in 4 districts of Punjab, namely Chakwal, Jhelum, Jhang and Layyah, as a pilot project potentially replicable throughout the province.</p>
				<p style="margin:10px 5px">
Based on interactions with stakeholders, a comprehensive review of the existing literature on child labour and its worst forms in Pakistan and globally and in light of the experience of ILO and other organisations in Pakistan, the project proposes a set of activities that are necessary to address the root causes of hazardous and worst forms of child labour. It includes ways and means to improve the access to social services for the children engaged in hazardous child labour and provision of some form of social or financial protection to the families of these children.</p>
<p style="margin:10px 5px"> The project also includes strategies to effectively link target families with the social service provided by the government, such as education, health, skill training. The project will also attempt to establish model workplaces free of hazards to safety and health, where young persons of legal age can learn the skills and work in safe manner and adults can work more productively. Although the scope of this project is limited to 4 districts, but the interventions are potentially replicable to other districts and sectors.
