<p style="margin:10px">
The Ministry of Labour through SRO 1280(I) 2005 dated 20 December, 2005 amended the Employment of Children Act 1991 through which the schedules of this Act has been changed to incorporate hazardous occupations and processes for child labour.</p>

<p style="margin:10px"> The new schedule is reproduced below:</p>

<div id="accordion"><h3>PART I - OCCUPATIONS</h3>
<div>

Any occupation - connected with
<ul class="objectiveUL" style="list-style:none">
<li>	transport of passengers, goods or mail;</li>
<li>	work in catering establishment at a railway station, involving the movement of a vendor or any other employee of the <span style="margin-left:7%">establishment from one platform to another or into or out of a moving train;</span></li>
<li>	work relating to the construction of railway station or with any other work where such work is done in close proximity<span style="margin-left:7%"> to or between the railway lines; and</span></li>
<li>	a port authority within the limits of any port.</li>
</ul>
</div>
